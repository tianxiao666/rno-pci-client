package com.hgicreate.rno.service;

import com.hgicreate.rno.model.Threshold;

import java.util.List;

/**
 * @author chen.c10
 */
public interface ThresholdService {
    /**
     * 通过模块类型获取阈值列表
     *
     * @param moduleType 模块类型
     * @return 阈值列表
     */
    List<Threshold> findByModuleType(Threshold.ModuleType moduleType);

    /**
     * 通过模版查询阈值列表
     *
     * @param threshold 查询模版
     * @return 阈值列表
     */
    List<Threshold> findAll(Threshold threshold);
}
